/**
 * @author Praveen
 * Class that defines all the global variables.
 *
 */
public class Globals {
	
	/**
	 * protected static variable that represents a 
	 * comment string 
	 */
	protected static final char COMMENT = '#';
	/**
	 * protected static variable that represents a value 
	 * of zero dollar
	 */
	protected static final int MODVALUE = 997;
	/**
	 * protected static variable that represents a value 
	 * of zero dollar
	 */
	protected static final double ZERODOLLAR = 0.00;
	
	/**
	 * protected static variable that represents a value 
	 * of -1 to denote that something went wrong
	 */
	protected static final int SOMETHINGWRONG = -1;
	
	/**
	 * protected static variable that represents a value 
	 * of 1 to denote that everything got executed as it should
	 */
	protected static final int OKAY = 1;
	
	/**
	 * protected static variable that represents end of category
	 */
	protected static final String ENDOFCATEGORY = "0";
	
	/**
	 * protected static variable that represents end of input
	 */
	protected static final String END = "END";
	
	/**
	 * protected static variable that represents insert operation
	 */
	protected static final String INSERT = "insert";
	
	/**
	 * protected static variable that represents delete operation
	 */
	protected static final String DELETE = "delete";
	
	/**
	 * protected static variable that represents add revenue operation
	 */
	protected static final String ADDREVENUE = "addrevenue";
	/**
	 * protected static variable that represents find operation
	 */
	protected static final String FIND = "find";
	
	/**
	 * protected static variable that represents addInterest operation
	 */
	protected static final String ADDINTEREST = "addinterest";
	/**
	 * protected static variable that represents removeInterest operation
	 */
	protected static final String REMOVEINTEREST = "removeinterests";
	
	/**
	 * protected static variable that represents top three operation
	 */
	protected static final String TOPTHREE = "topthree";
	
	
}
